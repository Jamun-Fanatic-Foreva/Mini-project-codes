function [rnew]=position(v,r,t)
        x=r(1);
        y=r(2);
        z=r(3);
        a=v;
        ax=a(1);
        ay=a(2);
        az=a(3);
        r=[x+(t*ax)/2,y+(t*ay)/2,z+(t*az)/2];
        b=velocity(a,r,t);
        bx=b(1);
        by=b(2);
        bz=b(3);
        r=x+(t*bx)/2,y+(t*by)/2,z+(t*bz)/2;
        c=velocity(b,r,t);
        cx=c(1);
        cy=c(2);
        cz=c(3);
        r=x+(t*cx),y+(t*cy),z+(t*cz);
        d=velocity(c,r,t);
        rnew=r+(t/6)*(a+2*b+2*c+d);
end